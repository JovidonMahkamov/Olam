class CustomerEntity {
  final int id;
  final String? fish;
  final String turi;
  final String? telefon;
  final String? manzil;
  final num qarzdorlik;
  final bool faol;
  final String yaratilgan;

  CustomerEntity({
    required this.id,
     this.fish,
    required this.turi,
     this.telefon,
     this.manzil,
    required this.qarzdorlik,
    required this.faol,
    required this.yaratilgan,
  });
}